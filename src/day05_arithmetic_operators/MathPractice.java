package day05_arithmetic_operators;

public class MathPractice{

    public static void main(String[] args) {
        System.out.println(5+9);
        System.out.println(4*5);//int * int = int
        System.out.println(2*5.5);//int * double = double
        System.out.println(10/3);//int  /int = int
        System.out.println(10/3.0);// int/double =double
        System.out.println(5 %2);// 2 + 2 =2 went thice into 5 there 1 left over
        System.out.println(4 %2);// no left behind
        System.out.println(6 % 2); //0 left
        System.out.println(4%10); // 4/10 . there not enough 10 to put in 4, so 4 is left over
        System.out.println(2%3);
        System.out.println(5%0.1);
    }
}
