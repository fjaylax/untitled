package day05_arithmetic_operators;

public class KgToPounds {
    public static void main(String[] args) {
        double lb1 =  2.20462;
        double kg20 =  (lb1 * 20);
        double kg10 =  (lb1 * 10);
        System.out.println(kg10);
        System.out.println("20 kg equal " +kg20+" lb and 10 kg equal "+kg10+" lb.");



    }
}
